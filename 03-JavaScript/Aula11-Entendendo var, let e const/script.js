
// Diferenças entre Var, Let e Const

//var -> cria variaveis, e pode ser acessada/alterada dentro e fora do escopo condicional

// let -> cria variaveis, e pode ser acessada/alterada dentro do escopo condicional, mas NAO pode ser acessada/alterada FORA do escopo condicional.

// const -> Cria uma CONSTANTE, após a criação não será possivel alterar seu valor.